			<div class="modal-body text-justify">
				<p align="justify">El Diario de los Debates del Senado tiene su antecedente histórico en la creación del Diario de los Debates de la Cámara de Diputados del Congreso de la Unión en 1867, cuando &ldquo;la crónica parlamentaria se institucionaliza a través de un medio oficial de los debates y actos parlamentarios. Antes de la creación de este medio, los periódicos de la época fueron los únicos vehículos para este fin; de esta manera se divulgaron los debates del Congreso mexicano durante la primera mitad del siglo XIX en periódicos como El Águila o El Sol, que daban cuenta no sólo de lo ocurrido en el Congreso, sino de la discusión, teorías y hechos detrás de los debates. Durante la primera mitad del siglo XIX fue igualmente muy común que los propios integrantes de los congresos y las legislaturas de los estados publicaran sus iniciativas, debates u opúsculos que incidieran en el trabajo legislativo y político de los congresos; de tal suerte, los dictámenes, votos y disensiones, así como las iniciativas de ley o reformas, eran publicadas en folletos que circulaban libremente fuera del recinto legislativo y que daban cuenta a la población de la labor de los parlamentarios&rdquo; (Manuel González Oropeza).</p>
<p align="justify">El Diario de los Debates del Senado concentra las deliberaciones y acuerdos adoptados en las sesiones del Pleno, pero también todos aquellos textos y documentos aludidos en las intervenciones a los cuales se puede acceder fácilmente en la versión digital.</p>
<p align="justify">Así, el Diario de los Debates, al difundir los debates parlamentarios, busca garantizar el ejercicio del derecho de acceso a la información establecido en nuestra Constitución, bajo el principio de máxima publicidad, considerada ésta como un principio constitucional de la democracia representativa.</p>
<p align="justify">FUNDAMENTO LEGAL<br>
  Ley Orgánica del Congreso General de los Estados Unidos Mexicanos, artículo 142, numeral 1. Reglamento del Senado, artículo 309.</p>

